function narrationFor(stop: TourStop, answers: GuidedTourAnswers) {
  if (answers.intent === "child") {
    return childNarrationFor(stop, answers.childAge);
  }

  const routeNarration =
    answers.intent === "skeptical"
      ? SKEPTICAL_NARRATION_BY_ID[stop.id]
      : answers.intent === "origin"
        ? ORIGIN_NARRATION_BY_ID[stop.id]
        : answers.intent === "ancestors"
          ? ANCESTOR_NARRATION_BY_ID[stop.id]
          : DEFAULT_NARRATION_BY_ID[stop.id];

  return routeNarration ?? `${stop.titleRu} стоит читать как живую сцену, а не как сухую подпись. ${stop.hintRu} На маршруте эта точка нужна, чтобы увидеть: прежние решения не исчезают, а получают новые роли в другой среде и у других потомков.`;
}

function questionFor(stop: TourStop, answers: GuidedTourAnswers) {
  if (answers.intent === "skeptical") {
    return "На что здесь стоит смотреть как на проверяемый след, а не как на мнение?";
  }
  if (answers.intent === "child") {
    return "Какая новая возможность появилась в этой части истории?";
  }
  if (stop.kind === "stage") {
    return "Какая черта этой остановки продолжает историю родства?";
  }

  return "Как эта страница помогает связать соседние остановки маршрута?";
}

