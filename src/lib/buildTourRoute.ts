function capitalize(value: string) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}

export function buildTourRoute(answers: GuidedTourAnswers): TourPlan {
  const budgetMin = answers.intent === "browse" ? null : clampBudget(answers);
  const stopIds =
    answers.intent === "browse"
      ? []
      : limitIds(routeIdsFor(answers, budgetMin ?? 5), budgetMin ?? 5);
  const steps = stopsFromIds(stopIds).map((stop) => ({
    ...stop,
    narrationRu: narrationFor(stop, answers),
    socraticQuestionRu: questionFor(stop, answers),
    lookAtRu: lookAtFor(stop),
    revealRu: revealFor(stop, answers),
  }));
  const planSeed = [
    answers.intent,
    budgetMin ?? "browse",
    answers.childAge ?? "",
    answers.freeText ?? "",
    steps.map((step) => step.id).join("|"),
  ].join("::");

  return {
    planId: `tour-${answers.intent}-${hashText(planSeed)}`,
    intent: answers.intent,
    budgetMin,
    childAge: answers.childAge ?? null,
    routeTitleRu: routeTitle(answers),
    pitchRu: pitchFor(answers),
    factsRu: factsFor(answers),
    introRu: introFor(answers),
    steps,
    browseLinks: answers.intent === "browse" ? GUIDED_TOUR_BROWSE_LINKS : undefined,
    nextSteps: nextStepsFor(answers) ?? [],
    outroRu: outroFor(answers),
  };
}
