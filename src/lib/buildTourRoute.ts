function hashText(text: string) {
  let hash = 5381;
  for (let index = 0; index < text.length; index += 1) {
    hash = (hash * 33) ^ text.charCodeAt(index);
  }
  return (hash >>> 0).toString(36);
}

function clampBudget(answers: GuidedTourAnswers) {
  return answers.budgetMin === 15 ? 15 : 5;
}

function targetStepCount(budgetMin: TourBudgetMin) {
  return budgetMin === 15 ? 15 : 8;
}

function limitIds(ids: string[], budgetMin: TourBudgetMin) {
  const target = targetStepCount(budgetMin);
  if (ids.length <= target) return ids;

  const first = ids[0];
  const last = ids.at(-1);
  const middle = ids.slice(1, -1);
  const middleSlots = Math.max(0, target - 2);
  const selected = new Set<string>();
  if (first) selected.add(first);

  for (let slot = 0; slot < middleSlots; slot += 1) {
    const ratio = middleSlots <= 1 ? 0 : slot / (middleSlots - 1);
    const index = Math.round(ratio * (middle.length - 1));
    const id = middle[index];
    if (id) selected.add(id);
  }

  if (last) selected.add(last);

  for (const id of ids) {
    if (selected.size >= target) break;
    selected.add(id);
  }

  return [...selected].slice(0, target);
}

