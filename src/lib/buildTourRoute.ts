function materialStep(id: string): TourNextStep {
  const material = PORTAL_MATERIALS.find((item) => item.id === id);
  if (!material) {
    throw new Error(`Unknown portal material: ${id}`);
  }

  return {
    kind: "presentation",
    labelRu: material.titleRu,
    href: material.pdfHref,
    descriptionRu: material.subtitleRu,
  };
}

function readingStep(id: string): TourNextStep {
  const book = READING_RECOMMENDATIONS.find((item) => item.id === id);
  if (!book) {
    throw new Error(`Unknown reading recommendation: ${id}`);
  }

  return {
    kind: "book",
    labelRu: book.titleRu,
    href: book.publisherHref,
    descriptionRu: book.whyReadRu,
  };
}

function watchStep(id: string): TourNextStep {
  const item = WATCH_RECOMMENDATIONS.find((recommendation) => recommendation.id === id);
  if (!item) {
    throw new Error(`Unknown watch recommendation: ${id}`);
  }

  return {
    kind: "video",
    labelRu: item.titleRu,
    href: item.href,
    descriptionRu: item.whyWatchRu,
  };
}

